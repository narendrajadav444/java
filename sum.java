class sum
{
	public static void main(String[] args)
	{
		int D1 = 100;
		int D2 = 111;
		
		int B1 = 0b1100100;
		int B2 = 0b0011011;
		
		int O1 = 0144;
		int O2 = 077;
		
		int H1 = 0x64;
		int H2 = 0x32;
		
		System.out.println("Sum of Two Decimal Number is: " + (D1+D2));
		System.out.println("Sum of Two Binary Number is: " + (B1+B2));
		System.out.println("Sum of Two Octal Number is: " + (O1+O2));
		System.out.println("Sum of Two Hexa Decimal Number is: " + (H1+H2));
	}
}