class demoLiterals
{
	public static void main(String[] args)
	{
		int decimalLiterals = 100;
		int binaryLiterals = 0b1100100;
		int octalLiterals = 0144;
		int hexaLiterals = 0x64;
		
		float floatLiterals = 2.728f;
		
		char charLiterals = 'A';
		String stringLiterals = "Hello, World!";
		boolean trueLiterals = true;
		boolean falseLiterals = false;
		
		System.out.println("Decimal is: " + decimalLiterals);
		System.out.println("Binary is: " + binaryLiterals);
		System.out.println("Octal is: " + octalLiterals);
		System.out.println("HexaDecimal is: " + hexaLiterals);
		System.out.println("Float is: " + floatLiterals);
		System.out.println("Char is: " + charLiterals);
		System.out.println("String is: " + stringLiterals);
		System.out.println("Boolean is: " + trueLiterals);
		System.out.println("Boolean is: " + falseLiterals);
		
	}
}